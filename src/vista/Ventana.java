    private void btn_modificarPanelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modificarPanelActionPerformed

        int fila = tbl_mascotas.getSelectedRow();

        if(fila >= 0){
            limpiarVentana();
            pnl_listar.setVisible(false);
            pnl_buscar.setVisible(false);
            pnl_almacenar.setVisible(false);
            pnl_modificar.setVisible(true);
            setTitle("MEDIPED - MODIFICAR MASCOTA");
            txt_idMod.setEditable(false);

            txt_idMod.setText(tbl_mascotas.getValueAt(fila,0).toString());
            txt_nombreMod.setText(tbl_mascotas.getValueAt(fila,1).toString());
            txt_edadMod.setText(tbl_mascotas.getValueAt(fila,2).toString());
            cmb_tipoMod.setSelectedItem((String)tbl_mascotas.getValueAt(fila, 3));

            if((boolean)tbl_mascotas.getValueAt(fila, 4)){
                chk_duenhoMod.setSelected(true);
                txt_duenhoMod.setEditable(true);
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "DEBE SELECCIONAR UNA FILA");
        }

    }//GEN-LAST:event_btn_modificarPanelActionPerformed

