    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        if(tipoBusqueda == TipoBusqueda.Nombre)
        {
            ArrayList<Mascota> listaMascotas = new ArrayList<>();
            ControladorMascota ctrlMascota = new ControladorMascota();

            //buscar de una caja de  texto
            listaMascotas = ctrlMascota.mascotaBuscarNombre(txt_buscar.getText());

            DefaultTableModel mascotaMostrar =
            (DefaultTableModel)tbl_mascotas.getModel();

            mascotaMostrar.setRowCount(0); //LIMPIA LA TABLA

            for(Mascota x: listaMascotas)
            {
                Object[] objs = {x.getIdMascota(), x.getNombreMascota(), x.getEdad()
                    ,x.getTipoMascota(),x.isCheckDuenho()};

                mascotaMostrar.addRow(objs);
            }
        }else if(tipoBusqueda == TipoBusqueda.Tipo)
        {
            ArrayList<Mascota> listaMascotas = new ArrayList<>();
            ControladorMascota ctrlMascota = new ControladorMascota();

            //buscar de un combox
            listaMascotas = ctrlMascota.mascotaBuscarTipo((String) cmb_tipoBuscar.getSelectedItem());

            DefaultTableModel mascotaMostrar =
            (DefaultTableModel)tbl_mascotas.getModel();

            mascotaMostrar.setRowCount(0); //LIMPIA LA TABLA

            for(Mascota x: listaMascotas)
            {
                Object[] objs = {x.getIdMascota(), x.getNombreMascota(), x.getEdad()
                    ,x.getTipoMascota(),x.isCheckDuenho()};

                mascotaMostrar.addRow(objs);
            }
        }

    }//GEN-LAST:event_btn_buscarActionPerformed

