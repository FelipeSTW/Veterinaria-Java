    //METODO QUE VALIDA REGLAS DE NEGOCIO
    private boolean validarReglasNegocio()
    {
        boolean indicador = true;
        
        try{
            
            String opcion = (String)cmb_tipo.getSelectedItem();
            
            if(opcion.equals("-Seleccione-")){
                indicador = false;
                JOptionPane.showMessageDialog(rootPane, "SELECCIONE UN TIPO");
            }
            
            if(txt_nombre.getText().equals("")){
                indicador = false;
                JOptionPane.showMessageDialog(rootPane, "FAVOR INGRESE NOMBRE");
            }
            
            int edad = 0;
            
            if(!txt_edad.getText().equals("")){
                edad = Integer.parseInt(txt_edad.getText());
            }
            
            if(edad < 0 || edad > 150){
                indicador = false;
                
            }
            
            
            
            int cantidad = 0;
            if(!txt_cantidad.getText().equals("")){
                cantidad = Integer.parseInt(txt_cantidad.getText());
            }
            
            if(cantidad < 0){
                indicador = false;
            }
            
            return indicador;
        }catch(Exception ex){
            indicador = false;
            return indicador;
        }
    }
    

