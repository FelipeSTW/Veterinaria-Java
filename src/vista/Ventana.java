    private void btn_almacenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_almacenarActionPerformed
        boolean validadorReglas = validarReglasNegocio();
        
            if(validadorReglas)//VERIFICAR POSTERIORMENTE REGLAS DE NEGOCIO
           { //LLAVE QUE ABRE REGLAS DE NEGOCIO
            Mascota mascota = new Mascota();
            ControladorMascota ctrlMascota = new ControladorMascota();
            boolean validador = false;


            mascota.setNombreMascota(txt_nombre.getText());

            if(!txt_edad.getText().equals("")) mascota.setEdad(Integer.parseInt(txt_edad.getText()));

           mascota.setTipoMascota((String)cmb_tipo.getSelectedItem());

            if(chk_atencion.isSelected()){
                mascota.setAtenPrevias(true);
                mascota.setCantAtenPrevias(Integer.parseInt(txt_cantidad.getText()));
            }

            if(chk_duenho.isSelected()){
                mascota.setCheckDuenho(true);
                mascota.setNombreDuenho(txt_duenho.getText());
            }

            if(!txt_descripcion.getText().equals("")){
                mascota.setDescripcion(txt_descripcion.getText());
            }else{
                mascota.setDescripcion("Sin Descripcion");
            }

            validador = ctrlMascota.agregarMascota(mascota);



            if(validador)
                {
                    JOptionPane.showMessageDialog(rootPane, "MASCOTA ALMACENADA EXITOSAMENTE");
                }
                else
                {
                    JOptionPane.showMessageDialog(rootPane, "ERROR AL ALMACENAR MASCOTA");
                }

                limpiarVentana();
                
            } //LLAVE QUE CIERRA REGLAS DE NEGOCIO
    }//GEN-LAST:event_btn_almacenarActionPerformed

